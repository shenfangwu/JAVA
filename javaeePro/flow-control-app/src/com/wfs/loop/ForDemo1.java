package com.wfs.loop;

/**
 * for循环语句
 */
public class ForDemo1 {
    public static void main(String[] args) {
        // For循环控制一段代码反复执行多次
        // For循环的常见应用场景是：1减少代码的重复编写，灵活的控制程序的执行 2批量生产数据
        for (int i = 0; i < 3; i++) {
            System.out.println("hangdanyuan101");
        }
    }
}
